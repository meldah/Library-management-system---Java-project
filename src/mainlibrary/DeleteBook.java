    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        
        String Pass = String.valueOf(password.getPassword());
        if(LibrarianDao.validate(userName.getText(),Pass))
        {
           
             int BookIDV = Integer.parseInt(bookID.getText());
             if(TransBookDao.CheckIssuedBook(BookIDV))
                 JOptionPane.showMessageDialog(DeleteBook.this, "Книгата е заета!","Грешка!", JOptionPane.ERROR_MESSAGE);     
        else
        {
             if(BookDao.Delete(BookIDV)!=0)
             { 
                 JOptionPane.showMessageDialog(DeleteBook.this, "Книгата е изтрита!","Изтрита!", JOptionPane.ERROR_MESSAGE);
                 userName.setText("");
                 password.setText("");
                 bookID.setText("");
             }
             else
                  JOptionPane.showMessageDialog(DeleteBook.this, "Неуспешно изтриване на книга!","Грешка!", JOptionPane.ERROR_MESSAGE); 
        }
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

